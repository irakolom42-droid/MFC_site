    // --- Функция для отрисовки услуг конкретной жизненной ситуации ---
    function renderLifeEventServices(eventName) {
        const eventData = lifeEventsData.find(e => e.name === eventName);
        if (!eventData) return;

        const breadcrumbs = renderBreadcrumbs(['Главная', 'Услуги', 'Жизненные ситуации', eventName]);
        
        // Находим ID услуг из общего списка по их названиям
        const serviceIds = eventData.services
            .map(serviceName => {
                const foundService = servicesData.find(s => s.name.includes(serviceName) || serviceName.includes(s.name));
                return foundService ? foundService.id : null;
            })
            .filter(id => id !== null);

        let servicesHtml = `
            ${breadcrumbs}
            <div class="life-event-services-page">
                <h2 class="life-event-services-title">Услуги по ситуации: ${eventName}</h2>
                <div class="services-list" id="life-event-services-list"></div>
            </div>
        `;
        rightContent.innerHTML = servicesHtml;

        const servicesList = document.getElementById('life-event-services-list');
        
        if (serviceIds.length === 0) {
            servicesList.innerHTML = '<p class="no-results">Услуги для данной ситуации не найдены.</p>';
            return;
        }

        // Отображаем услуги, используя данные из общего списка
        const servicesToShow = servicesData.filter(s => serviceIds.includes(s.id));
        
        servicesList.innerHTML = '';
        servicesToShow.forEach(s => {
            const card = document.createElement('div');
            card.className = 'service-card';
            card.dataset.serviceId = s.id;
            card.innerHTML = `
                <h4>${s.name}</h4>
                <div class="service-org">${s.org}</div>
            `;
            card.addEventListener('click', () => renderServiceDetail(s.id, s.name));
            servicesList.appendChild(card);
        });
    }

